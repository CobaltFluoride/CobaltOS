BITS 16
org 0x7C00

start:
	call SetupVideo
	mov si, WelcomeMessage
	call print
	call drawBox
	jmp $

drawBox:
	mov ah, 0x0e
.widthSides:
	mov si, TopLeftCorner
	lodsb
	mov bl, 0
	int 0x10
.heightSides:

	ret


print:
	mov ah, 0x0E
.loop:
	lodsb
	cmp al, 0
	je .done
	mov bl, 0
	int 0x10
	jmp .loop
.done:
	ret

SetupVideo:
	mov ah, 0x00
	mov al, 0x07
	int 0x10
	ret

end:

	TopLeftCorner db 0xC9
	TopWall db 0xCD
	TopRightCorner db 0xBB
	BottomLeftCorner db 0xC8
	BottomRightCorner db 0xBC
	WelcomeMessage db 0xC9, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xBB, 0x0a, 0x0d, 0xBA, "CobaltOS", 0xBA, 0x0a, 0x0d, 0xC8, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xCD, 0xBC, 0

	times 510 - ($-$$) db 0x00
	db 0x55, 0xAA
